const toSlug = (value: string) => {
  const normalized = value
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
  return normalized || "image";
};

const slugCounts = new Map<string, number>();

export const galleryItems: GalleryItem[] = gallerySource.map(({ slug, title, image }) => {
  // If no slug provided, auto-generate from title
  if (!slug) {
    const base = toSlug(title);
    const nextCount = (slugCounts.get(base) ?? 0) + 1;
    slugCounts.set(base, nextCount);
    slug = nextCount > 1 ? `${base}-${nextCount}` : base;
  }
  return { slug, title, image };
});
